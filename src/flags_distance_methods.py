# Function to calculate normalized MSE and SSIM between two images
def get_images_distance_SSIM(image_generated, image_ref):
    
    if isinstance(image_generated, np.ndarray):
        img1 = image_generated
        img2 = image_ref
    else:
        img1 = load_image_from_path(image_generated)
        img2 = load_image_from_path(image_ref)

    # Ensure both images have the same dimensions
    if img1.shape != img2.shape:
        raise ValueError("Images must have the same dimensions for comparison.")
    
    # Compute SSIM
    ssim_similarity = structural_similarity(img1, img2, channel_axis=None) # single-channel (greyscale)
    # ssim_similarity = structural_similarity(img1, img2, channel_axis=-1) # RGB
    
    return 1.0 - ssim_similarity

#---------------------------------------------------

def get_images_distance_MSE(image_generated, image_ref):
    
    if isinstance(image_generated, np.ndarray):
        img1 = image_generated
        img2 = image_ref
    else:
        img1 = load_image_from_path(image_generated)
        img2 = load_image_from_path(image_ref)

    # Ensure both images have the same dimensions
    if img1.shape != img2.shape:
        raise ValueError("Images must have the same dimensions for comparison.")
    
    # Compute MSE
    mse_value = mean_squared_error(img1, img2)
    
    # Normalize MSE
    max_mse = 255 ** 2 * 3  # Maximum possible MSE for RGB images
    normalized_mse = mse_value / max_mse

    return normalized_mse

