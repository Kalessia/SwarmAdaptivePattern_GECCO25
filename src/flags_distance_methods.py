# Function to load an image from a file path and convert it to a numpy array
def load_image_from_path(image_path):
    return np.array(Image.open(image_path).convert('RGB'))

#---------------------------------------------------

def convert_flag_to_image(flag, grid_nb_rows, grid_nb_cols):

    # Convert data to a NumPy array and reshape the data array into 2D dimensions
    data_array = np.array(flag)
    data_2d = data_array.reshape((grid_nb_rows, grid_nb_cols))

    # Normalize data to 0-255 and convert to unsigned 8-bit integers
    data_2d_uint8 = (data_2d * 255).astype(np.uint8)

    # Create an image from the 2D array (to show or plot)
    # image = Image.fromarray(data_2d_uint8, mode='L') # 'L' stands for Luminance or grayscale (single-channel grayscale image). Pixel values are integers between 0 (black) and 255 (white).
    # image = Image.fromarray(data_2d_uint8, mode='RGB') # 'RGB' for a colored image
    # image.save('output.png')

    return data_2d_uint8 # numpy array representing the image

