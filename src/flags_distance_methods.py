# Function to calculate CLIP similarity between two images
def get_images_distance_CLIP(image_generated, image_ref):
    
    # Load CLIP model and preprocessing
    device = "cuda" if torch.cuda.is_available() else "cpu"
    model, preprocess = clip.load("ViT-B/32", device=device)

    # Load and preprocess images for CLIP
    if isinstance(image_generated, np.ndarray):
        img1 = preprocess(Image.fromarray(image_generated)).unsqueeze(0).to(device)
        img2 = preprocess(Image.fromarray(image_ref)).unsqueeze(0).to(device)
    else: # argument are images paths
        img1 = preprocess(Image.open(image_generated)).unsqueeze(0).to(device)
        img2 = preprocess(Image.open(image_ref)).unsqueeze(0).to(device)
    
    # Encode the images
    with torch.no_grad():
        img1_features = model.encode_image(img1)
        img2_features = model.encode_image(img2)
    
    # Normalize the features
    img1_features /= img1_features.norm(dim=-1, keepdim=True)
    img2_features /= img2_features.norm(dim=-1, keepdim=True)
    
    # Compute cosine similarity
    clip_similarity = (img1_features @ img2_features.T).item()
    
    return 1.0 - clip_similarity

